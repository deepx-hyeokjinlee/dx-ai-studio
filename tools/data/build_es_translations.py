def build_translations(en_strings: list[str]) -> dict[str, str]:
    if len(ES_VALUES) != len(en_strings):
        raise ValueError(
            f"Translation count mismatch: {len(ES_VALUES)} ES vs {len(en_strings)} EN"
        )
    return dict(zip(en_strings, ES_VALUES, strict=True))


# Full ES dict keyed by exact English source strings (built on demand).
ES_TRANSLATIONS: dict[str, str] = {}


def get_es_translations() -> dict[str, str]:
    global ES_TRANSLATIONS
    if not ES_TRANSLATIONS:
        ES_TRANSLATIONS = build_translations(load_en_strings())
    return ES_TRANSLATIONS


def main() -> int:
    try:
        en_strings = load_en_strings()
    except FileNotFoundError as exc:
        print(f"ERROR: {exc}", file=sys.stderr)
        return 1

    translations = get_es_translations()

    missing = [s for s in en_strings if s not in translations]
    extra = [k for k in translations if k not in en_strings]
    if missing:
        print(f"ERROR: {len(missing)} missing translation keys", file=sys.stderr)
        for s in missing[:5]:
            print(f"  - {s!r}", file=sys.stderr)
        return 1
    if extra:
        print(f"ERROR: {len(extra)} extra translation keys", file=sys.stderr)
        return 1

    OUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    with OUT_PATH.open("w", encoding="utf-8") as f:
        json.dump(translations, f, ensure_ascii=False, indent=2)
        f.write("\n")

    with OUT_PATH.open(encoding="utf-8") as f:
        loaded = json.load(f)
    assert len(loaded) == 430, f"Expected 430 entries, got {len(loaded)}"
    assert set(loaded.keys()) == set(en_strings), "Key mismatch after write"

    print(f"Wrote {OUT_PATH}")
    print(f"Entries: {len(loaded)}")
    print("Verification: OK (json.loads, len=430, all EN keys present)")
    return 0


if __name__ == "__main__":
    sys.exit(main())
