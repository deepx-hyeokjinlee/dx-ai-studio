// This callback is invoked by shared/i18n.js when the UI language changes.
// It re-applies selector-based DOM translations defined in _DX_I18N_SELECTORS
// via the shared DXI18n.applyLang() — not a redundant traversal but the
// required mechanism for modules that declare selectors + dict only.
function refreshHelpTips() {
  if (typeof T !== 'function') return;
  document.querySelectorAll('.help-tip[data-i18n-tip]').forEach(function (el) {
    var key = el.getAttribute('data-i18n-tip');
    if (!key) return;
    var tip = T(key);
    el.setAttribute('data-tip', tip);
    el.setAttribute('title', tip);
  });
}

